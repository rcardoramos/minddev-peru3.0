let currentLanguage = 'es';

// Función para aplicar las traducciones
function applyTranslations(lang) {
    // 1. Actualizar el atributo lang del HTML
    document.documentElement.lang = lang;
    currentLanguage = lang;

    const keys = translations[lang];

    // 2. Actualizar elementos con data-text-key
    document.querySelectorAll('[data-text-key]').forEach(el => {
        const key = el.getAttribute('data-text-key');
        if (keys[key]) {
            // Si el elemento contiene HTML (como los span resaltados en el FAQ/Testimonios)
            if (el.tagName === 'P' || el.tagName === 'H2' || el.tagName === 'H3' || el.tagName === 'A' || el.tagName === 'SPAN' || el.tagName === 'LABEL') {
                el.innerHTML = keys[key];
            } else {
                el.textContent = keys[key];
            }
        }
    });

    // 3. Actualizar placeholders (no se usa innerHTML/textContent)
    document.querySelectorAll('[data-placeholder-key]').forEach(el => {
        const key = el.getAttribute('data-placeholder-key');
        if (keys[key]) {
            el.placeholder = keys[key];
        }
    });

    // 4. Actualizar opciones del Dropdown del Formulario (Options necesitan ser rellenados)
    const serviceOptions = {
        'option_select_service': keys.option_select_service,
        'option_web_dev': keys.option_web_dev,
        'option_mobile_app': keys.option_mobile_app,
        'option_maint_seo': keys.option_maint_seo,
        'option_hosting': keys.option_hosting,
        'option_training': keys.option_training,
        'option_alliance': keys.option_alliance
    };

    const selectEl = document.getElementById('service_interest');
    if (selectEl) {
        // Iterar sobre las opciones existentes y actualizar su texto
        Array.from(selectEl.options).forEach(option => {
            const optionKey = option.getAttribute('data-option-key');
            if (optionKey && serviceOptions[optionKey]) {
                option.textContent = serviceOptions[optionKey];
            }
        });
    }


    // 5. Re-renderizar iconos (necesario si se usa data-lucide en elementos dinámicos)
    lucide.createIcons();
}